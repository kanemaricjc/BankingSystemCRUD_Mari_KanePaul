    void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); 
    
    CustomerDAO dao = new CustomerDAO();
    List<Customer> list = dao.getAllCustomers();
    for (Customer c : list) {
        Object[] row = {
            c.getCustomerid(),
            c.getFname(),
            c.getLname(),
            c.getEmail(),
            c.getPhonenumber()
        };      
        model.addRow(row);
    }
    }
    
